package archivoscine;

public class listaConsultas {
    
    private int nSalas;
    private String nomPeli;
    
    public listaConsultas(){
        nSalas=0;
        nomPeli="";
    }

    public int getnSalas() {
        return nSalas;
    }

    public void setnSalas(int nSalas) {
        this.nSalas = nSalas;
    }

    public String getNomPeli() {
        return nomPeli;
    }

    public void setNomPeli(String nomPeli) {
        this.nomPeli = nomPeli;
    }
    
     
    
    
}
